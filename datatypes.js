// Primitive datatypes

// String
var str = "Hello";
console.log(str); // Prints "Hello"

// Number
var num = 10.1;
console.log(num); // Prints 10.1

// Boolean
var t = true;
console.log(t); // Prints false

// Null
var n = null;
console.log(n); // Prints null

// Undefined
let g;
console.log(g); // Prints undefined

// Non-primitive datatypes

// Array
var arr = ["chennai", 1, 23.5, true, null];
console.log(arr[2]); // Prints 23.5



// Object
var obj = {
    name: "hari",
    age: 10,
    std: "v"
};
console.log(obj); // Prints the entire object


